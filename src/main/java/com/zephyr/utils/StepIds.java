package com.zephyr.utils;

public class StepIds {

	Integer orderId;

	String testStepId;
	
	String testStepResultId;

	public Integer getOrderId() {
		return orderId;
	}

	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}

	public String getTestStepId() {
		return testStepId;
	}

	public void setTestStepId(String testStepId) {
		this.testStepId = testStepId;
	}

	public String getTestStepResultId() {
		return testStepResultId;
	}

	public void setTestStepResultId(String testStepResultId) {
		this.testStepResultId = testStepResultId;
	}
	
	
	

}
